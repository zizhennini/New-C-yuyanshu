/*
有一个神奇的迭代公式：Xn+1=sqrt(Xn+2) ，无论x的初值（大于2的正数）选的多么大，若干次迭代之后，
x都与2无限接近，也就是说x序列的极限是2。假设x0=99999999.0(8个9)，编程输入一个
正整数n，输出xn的值（保留10位小数）。
输入样例:8
输出样例:x[8]=2.0051798692
输入样例:16
输出样例:x[16]=2.0000000790


*/

#include <stdio.h>
#include <math.h>

int main() {
	double x = 99999999.0; 
	int n;

	
	scanf("%d", &n);

	for (int i=1;i<=n;i++){
		x = sqrt(x+2);
		
	}
	printf("x[%d]=%.10lf",n,x);
	
	return 0;
}